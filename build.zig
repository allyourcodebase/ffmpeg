const CategorizedSources = struct {
    avcodec: []const []const u8,
    avdevice: []const []const u8,
    avfilter: []const []const u8,
    avformat: []const []const u8,
    avutil: []const []const u8,
    swresample: []const []const u8,
    swscale: []const []const u8,
};

/// For x86, files ending in .asm are omitted.
fn categorizeSources(ally: std.mem.Allocator, target: std.Target) CategorizedSources {
    var libs: [@typeInfo(CategorizedSources).Struct.fields.len]struct {
        prefix: []const u8,
        list: std.ArrayListUnmanaged([]const u8) = .{},
    } = undefined;

    inline for (@typeInfo(CategorizedSources).Struct.fields, 0..) |field, i| {
        libs[i] = .{ .prefix = "lib" ++ field.name ++ "/" };
    }
    for (all_sources) |prefixed_path| {
        const path = if (std.mem.startsWith(u8, prefixed_path, "/L/")) p: {
            if (target.os.tag != .linux) continue;
            break :p prefixed_path["/L/".len..];
        } else if (std.mem.startsWith(u8, prefixed_path, "/W/")) p: {
            if (target.os.tag != .windows) continue;
            break :p prefixed_path["/W/".len..];
        } else prefixed_path;

        const lib = for (&libs) |*lib| {
            if (std.mem.startsWith(u8, path, lib.prefix))
                break lib;
        } else {
            std.debug.panic("source file '{s}' is not prefixed with any ffmpeg library name", .{
                path,
            });
        };

        const sub_path = path[lib.prefix.len..];

        // Skip files from wrong targets.
        if (std.mem.startsWith(u8, sub_path, "aarch64/")) {
            if (!target.cpu.arch.isAARCH64()) continue;
        } else if (std.mem.startsWith(u8, sub_path, "alpha/")) {
            continue;
        } else if (std.mem.startsWith(u8, sub_path, "arm/")) {
            if (!target.cpu.arch.isARM()) continue;
        } else if (std.mem.startsWith(u8, sub_path, "alpha/")) {
            continue;
        } else if (std.mem.startsWith(u8, sub_path, "avr32/")) {
            continue;
        } else if (std.mem.startsWith(u8, sub_path, "bfin/")) {
            continue;
        } else if (std.mem.startsWith(u8, sub_path, "cuda/")) {
            continue;
        } else if (std.mem.startsWith(u8, sub_path, "dnn/")) {
            // This is a component subdirectory, not a target one.
        } else if (std.mem.startsWith(u8, sub_path, "loongarch/")) {
            switch (target.cpu.arch) {
                .loongarch32, .loongarch64 => {},
                else => continue,
            }
        } else if (std.mem.startsWith(u8, sub_path, "metal/")) {
            continue;
        } else if (std.mem.startsWith(u8, sub_path, "mips/")) {
            if (!target.cpu.arch.isMIPS()) continue;
        } else if (std.mem.startsWith(u8, sub_path, "neon/")) {
            switch (target.cpu.arch) {
                .arm, .armeb => {
                    if (!std.Target.arm.featureSetHas(target.cpu.features, .neon))
                        continue;
                },
                .aarch64, .aarch64_be, .aarch64_32 => {
                    if (!std.Target.aarch64.featureSetHas(target.cpu.features, .neon))
                        continue;
                },
                else => continue,
            }
        } else if (std.mem.startsWith(u8, sub_path, "opencl/")) {
            continue;
        } else if (std.mem.startsWith(u8, sub_path, "ppc/")) {
            if (!target.cpu.arch.isPPC()) continue;
        } else if (std.mem.startsWith(u8, sub_path, "riscv/")) {
            if (!target.cpu.arch.isRISCV()) continue;
        } else if (std.mem.startsWith(u8, sub_path, "sh4/")) {
            continue;
        } else if (std.mem.startsWith(u8, sub_path, "sparc/")) {
            if (!target.cpu.arch.isSPARC()) continue;
        } else if (std.mem.startsWith(u8, sub_path, "x86/")) {
            if (!target.cpu.arch.isX86()) continue;
            // These are handled separately because they are passed through nasm.
            if (std.mem.endsWith(u8, sub_path, ".asm")) continue;
        } else if (std.mem.indexOfScalar(u8, sub_path, '/') != null) {
            std.debug.panic("unrecognized subdirectory in path '{s}'", .{path});
        }

        lib.list.append(ally, path) catch @panic("OOM");
    }

    var result: CategorizedSources = undefined;
    inline for (@typeInfo(CategorizedSources).Struct.fields, 0..) |field, i| {
        @field(result, field.name) = libs[i].list.items;
    }
    return result;
}


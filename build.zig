fn fastUnalignedLoads(t: std.Target) bool {
    return switch (t.cpu.arch) {
        .x86_64 => true,
        else => false,
    };
}

fn have_x86_feat(t: std.Target, feat: std.Target.x86.Feature) c_int {
    return @intFromBool(switch (t.cpu.arch) {
        .x86, .x86_64 => std.Target.x86.featureSetHas(t.cpu.features, feat),
        else => false,
    });
}

fn have_arm_feat(t: std.Target, feat: std.Target.arm.Feature) c_int {
    return @intFromBool(switch (t.cpu.arch) {
        .arm, .armeb => std.Target.arm.featureSetHas(t.cpu.features, feat),
        else => false,
    });
}

fn have_aarch64_feat(t: std.Target, feat: std.Target.aarch64.Feature) c_int {
    return @intFromBool(switch (t.cpu.arch) {
        .aarch64,
        .aarch64_be,
        .aarch64_32,
        => std.Target.aarch64.featureSetHas(t.cpu.features, feat),

        else => false,
    });
}

fn have_ppc_feat(t: std.Target, feat: std.Target.powerpc.Feature) c_int {
    return @intFromBool(switch (t.cpu.arch) {
        .powerpc,
        .powerpcle,
        .powerpc64,
        .powerpc64le,
        => std.Target.powerpc.featureSetHas(t.cpu.features, feat),

        else => false,
    });
}

fn have_mips_feat(t: std.Target, feat: std.Target.mips.Feature) c_int {
    return @intFromBool(switch (t.cpu.arch) {
        .mips,
        .mipsel,
        .mips64,
        .mips64el,
        => std.Target.mips.featureSetHas(t.cpu.features, feat),

        else => false,
    });
}

fn missing_mips_feat(t: std.Target, feat: std.Target.mips.Feature) c_int {
    return @intFromBool(switch (t.cpu.arch) {
        .mips,
        .mipsel,
        .mips64,
        .mips64el,
        => !std.Target.mips.featureSetHas(t.cpu.features, feat),

        else => false,
    });
}

fn basenameNewExtension(b: *std.Build, path: []const u8, new_extension: []const u8) []const u8 {
    const basename = std.fs.path.basename(path);
    const ext = std.fs.path.extension(basename);
    return b.fmt("{s}{s}", .{ basename[0 .. basename.len - ext.len], new_extension });
}

